// ─── Utility: Sanitize JD into a clean paragraph ───
function cleanJobDescription(raw) {
    if (!raw) return "";
    return raw
        .replace(/\r\n/g, "\n")
        .replace(/\n{2,}/g, "\n")
        .replace(/\n/g, " ")
        .replace(/\s{2,}/g, " ")
        .replace(/\t/g, " ")
        .trim();
}

// ─── PDF Generator ───
function downloadPdf(letterText, index) {
    const doc = new jsPDF({ unit: "mm", format: "a4" });
    const pageWidth = doc.internal.pageSize.getWidth();
    const margin = 20;
    const usableWidth = pageWidth - margin * 2;
    const lineHeight = 7;
    let y = margin + 10;

    doc.setFont("helvetica", "normal");
    doc.setFontSize(11);

    const lines = doc.splitTextToSize(letterText, usableWidth);

    for (const line of lines) {
        if (y + lineHeight > doc.internal.pageSize.getHeight() - margin) {
            doc.addPage();
            y = margin;
        }
        doc.text(line, margin, y);
        y += lineHeight;
    }

    // Footer
    doc.setFontSize(8);
    doc.setTextColor(160);
    doc.text("Generated by CovGen", margin, doc.internal.pageSize.getHeight() - 10);

    doc.save(`cover-letter-${index + 1}.pdf`);
}

