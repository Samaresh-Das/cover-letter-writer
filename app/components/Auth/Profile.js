// ─── Letter Card Component ───
function SavedLetterCard({ letter, index, userPlan, onDelete }) {
    const [showJd, setShowJd] = useState(false);
    const [deleting, setDeleting] = useState(false);
    const [confirmDelete, setConfirmDelete] = useState(false);

    const handleCopy = () => {
        navigator.clipboard.writeText(letter.generatedText);
        toast.success("Copied to clipboard!");
    };

    const handleDownload = () => {
        if (userPlan !== "pro") {
            toast("Upgrade to Pro to download PDFs", {
                icon: "🔒",
                style: {
                    border: "1px solid #f59e0b",
                    padding: "12px 16px",
                    color: "#92400e",
                    background: "#fffbeb",
                    fontWeight: "600",
                },
            });
            return;
        }
        downloadPdf(letter.generatedText, index);
        toast.success("PDF downloaded!");
    };

    const handleDelete = async () => {
        if (!confirmDelete) {
            setConfirmDelete(true);
            setTimeout(() => setConfirmDelete(false), 3000);
            return;
        }
        setDeleting(true);
        try {
            const token = localStorage.getItem("token");
            const res = await fetch(
                `${process.env.NEXT_PUBLIC_API_BASE_URL}/api/coverletters/${letter._id}`,
                {
                    method: "DELETE",
                    headers: { Authorization: `Bearer ${token}` },
                }
            );
            if (res.ok) {
                toast.success("Letter deleted");
                onDelete(letter._id);
            } else {
                toast.error("Failed to delete");
            }
        } catch {
            toast.error("Network error");
        } finally {
            setDeleting(false);
            setConfirmDelete(false);
        }
    };

    const expiryHours = letter.expiresIn;
    const expiryLabel = expiryHours !== null && expiryHours !== undefined
        ? expiryHours > 24
            ? `${Math.ceil(expiryHours / 24)}d left`
            : expiryHours > 0
                ? `${expiryHours}h left`
                : "Expiring soon"
        : null;

    return (
        <motion.div
            initial={{ opacity: 0, y: 20 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, y: -20, scale: 0.95 }}
            transition={{ duration: 0.3, delay: index * 0.05 }}
            layout
            className="group bg-white rounded-3xl p-6 border border-slate-200/60 shadow-[0_4px_24px_rgba(0,0,0,0.04)] hover:shadow-[0_8px_40px_rgba(59,130,246,0.08)] hover:border-blue-200/60 transition-all duration-300"
        >
            {/* Header */}
            <div className="flex items-start justify-between mb-4">
                <div>
                    <span className="text-[10px] font-bold uppercase tracking-widest text-blue-500">
                        {letter.modelUsed || "AI Generated"}
                    </span>
                    <p className="text-xs text-slate-400 mt-1">
                        {new Date(letter.createdAt).toLocaleDateString("en-US", {
                            month: "short", day: "numeric", year: "numeric",
                        })} • {new Date(letter.createdAt).toLocaleTimeString("en-US", {
                            hour: "2-digit", minute: "2-digit",
                        })}
                    </p>
                </div>
                <div className="flex items-center gap-2">
                    {expiryLabel && (
                        <span className="text-[10px] font-bold px-2.5 py-1 rounded-full bg-amber-50 text-amber-600 border border-amber-100">
                            ⏳ {expiryLabel}
                        </span>
                    )}
                    {letter.plan === "pro" && (
                        <span className="text-[10px] font-bold px-2.5 py-1 rounded-full bg-purple-50 text-purple-600 border border-purple-100 flex items-center gap-1">
                            <FaCrown className="w-2.5 h-2.5" /> Pro
                        </span>
                    )}
                </div>
            </div>

            {/* Letter preview */}
            <div className="mb-4">
                <p className="text-sm text-slate-700 leading-relaxed line-clamp-5">
                    {letter.generatedText}
                </p>
            </div>

            {/* JD Toggle */}
            <button
                onClick={() => setShowJd(!showJd)}
                className="w-full flex items-center justify-between px-4 py-2.5 rounded-xl bg-slate-50 hover:bg-slate-100 border border-slate-200/60 text-xs font-semibold text-slate-500 transition-all duration-200 mb-4 cursor-pointer"
            >
                <span>📋 View Job Description</span>
                {showJd ? <FaChevronUp className="w-3 h-3" /> : <FaChevronDown className="w-3 h-3" />}
            </button>

            <AnimatePresence>
                {showJd && (
                    <motion.div
                        initial={{ height: 0, opacity: 0 }}
                        animate={{ height: "auto", opacity: 1 }}
                        exit={{ height: 0, opacity: 0 }}
                        transition={{ duration: 0.25 }}
                        className="overflow-hidden mb-4"
                    >
                        <div className="p-4 bg-slate-50 rounded-xl border border-slate-200/60 text-sm text-slate-600 leading-relaxed max-h-40 overflow-y-auto"
                            style={{ scrollbarWidth: "thin" }}
                        >
                            {cleanJobDescription(letter.jobDescription)}
                        </div>
                    </motion.div>
                )}
            </AnimatePresence>

            {/* Action Buttons */}
            <div className="flex items-center gap-2 pt-2 border-t border-slate-100">
                <button
                    onClick={handleCopy}
                    className="flex-1 flex items-center justify-center gap-1.5 px-3 py-2 rounded-xl text-xs font-semibold bg-blue-50 text-blue-600 hover:bg-blue-100 border border-blue-100 transition-all duration-200 active:scale-95 cursor-pointer"
                >
                    <FaCopy className="w-3 h-3" /> Copy
                </button>
                <button
                    onClick={handleDownload}
                    className={`flex-1 flex items-center justify-center gap-1.5 px-3 py-2 rounded-xl text-xs font-semibold transition-all duration-200 active:scale-95 cursor-pointer border ${
                        userPlan === "pro"
                            ? "bg-emerald-50 text-emerald-600 hover:bg-emerald-100 border-emerald-100"
                            : "bg-slate-50 text-slate-400 border-slate-200 cursor-not-allowed"
                    }`}
                >
                    {userPlan === "pro" ? (
                        <><FaDownload className="w-3 h-3" /> PDF</>
                    ) : (
                        <><FaLock className="w-3 h-3" /> PDF</>
                    )}
                </button>
                <button
                    onClick={handleDelete}
                    disabled={deleting}
                    className={`flex items-center justify-center gap-1.5 px-3 py-2 rounded-xl text-xs font-semibold transition-all duration-200 active:scale-95 cursor-pointer border ${
                        confirmDelete
                            ? "bg-red-500 text-white border-red-500 hover:bg-red-600"
                            : "bg-red-50 text-red-500 hover:bg-red-100 border-red-100"
                    } ${deleting ? "opacity-50 cursor-not-allowed" : ""}`}
                >
                    <FaTrash className="w-3 h-3" />
                    {confirmDelete ? "Sure?" : ""}
                </button>
            </div>
        </motion.div>
    );
}

